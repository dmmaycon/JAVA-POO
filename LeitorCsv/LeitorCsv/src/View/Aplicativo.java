    private void btnLeCsvActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLeCsvActionPerformed
        String caminho = txtCaminhoArq.getText();
        String separador = txtSeparador.getText();
        int qtdColunas = Integer.parseInt(txtNumColunas.getText());
        try {
            LeitoraCsv lcsv = new LeitoraCsv(caminho, separador, qtdColunas);
            ArrayList<String> result = lcsv.lerArquivo();
            String a = "";
            for(int x = 0; x < result.size(); x++){
                a += result.get(x);
                a += "\n";
                //System.out.println(result.get(x));
            }
            txaRecebeCsv.setText(a);
            a = "";
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Aplicativo.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(Aplicativo.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnLeCsvActionPerformed

