    private void btnSelecionarArqActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelecionarArqActionPerformed
        JFileChooser file = new JFileChooser(); 
          file.setFileSelectionMode(JFileChooser.FILES_ONLY);
          
          int i= file.showSaveDialog(null);
        if (i==1){
            txtCaminhoArq.setText("");
        } else {
            File arquivo = file.getSelectedFile();
           txtCaminhoArq.setText(arquivo.getPath());
        }
          
    }//GEN-LAST:event_btnSelecionarArqActionPerformed

